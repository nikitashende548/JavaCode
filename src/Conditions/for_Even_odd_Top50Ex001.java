package Conditions;

public class for_Even_odd_Top50Ex001 {
    public static void main(String[] args) {
        for(int i=0;i<=50;i++ )
            if(i%2==0)
            {
                System.out.println(i +" = is an Even Number");
            }
            else
            {
                System.out.println(i + "= is an Odd Number");
            }
    }
}
